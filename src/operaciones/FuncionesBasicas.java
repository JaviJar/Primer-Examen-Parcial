package operaciones;

public class FuncionesBasicas {
    public double sumar(double parametro1, double parametro2){return parametro1 + parametro2;}

    public double restar(double parametro1, double parametro2){return parametro1 - parametro2;}

    public double multiplicar(double parametro1, double parametro2){return parametro1 * parametro2;}

    public double dividir(double parametro1, double parametro2){return parametro1 / parametro2;}
}
